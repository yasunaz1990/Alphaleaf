package com.variable;

public class VariableDemo {

    // Main method, executed by CPU
    public static void main(String[] args)
    {
        // Integer, Double, Char, String, Boolean

        // fav: 7
        int myFavNumber = 7;

        // character: !
        char supriseSymbol = '!';

        // fact: real madrid is best
        //  best   --> true
        //  worst  --> false
        boolean isRealMadridBest = true;

        // pi = 3.1415
        double piValue = 3.1415;

        //
        String greeting = "hello world!";


        System.out.println( myFavNumber );
        System.out.println( supriseSymbol );
        System.out.println( isRealMadridBest);
        System.out.println( piValue);
        System.out.println( greeting);

    }
}
